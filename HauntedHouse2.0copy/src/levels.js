import Block from "./block.js"
import Door from "./door.js"
import Ghost from "./ghost.js"
import Skeleton from "./skeleton.js"
import Fire from "./fire.js"

export function buildObjects(game, level) {
  let blocks = [[],[],[],[],[]]
  level.forEach((row, rowIndex) => {
    row.forEach((block, blockIndex) => {
      if (block === 1) {
        let pos = {
          x: game.block.width * blockIndex,
          y: game.block.height * rowIndex
        }
        blocks[0].push(new Block(game, pos))
      } else if (block === 2) {
        let pos = {
          x: game.block.width * blockIndex,
          y: game.block.height * rowIndex
        }
        blocks[1].push(new Door(game, pos))
      } else if (block === 3) {
        let pos = {
          x: game.block.width * blockIndex,
          y: game.block.height * rowIndex
        }
        blocks[2].push(new Ghost(game, pos))
      } else if (block === 4) {
        let pos = {
          x: game.block.width * blockIndex,
          y: game.block.height * rowIndex
        }
        blocks[3].push(new Skeleton(game, pos))
      } else if (block === 5) {
        let pos = {
          x: game.block.width * blockIndex,
          y: game.block.height * rowIndex
        }
        blocks[4].push(new Fire(game, pos))
      }
    })
  })
  return blocks
}
